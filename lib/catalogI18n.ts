function applyPhraseMap(text: string, language: Exclude<LanguageCode, "zh">) {
  return Object.entries(phrases[language])
    .sort(([left], [right]) => right.length - left.length)
    .reduce((result, [source, target]) => result.split(source).join(target), text);
}

export function translateCatalogText(text: string, language: LanguageCode, kind: CatalogTextKind = "general") {
  if (language === "zh" || !text) return text;
  if (kind === "description" && text.includes("压缩海绵沙发")) {
    return commonDescriptions[language];
  }

  return applyPhraseMap(text, language)
    .replace(/\s*\/\s*/g, " / ")
    .replace(/\s*;\s*/g, "; ")
    .replace(/，/g, ", ")
    .replace(/、/g, ", ")
    .replace(/。/g, ".")
    .replace(/\s{2,}/g, " ")
    .trim();
}
